    /(^\s*我|我想|我要|我会|我准备|我打算).{0,18}(你|你的|你们|您|贵方)/.test(text);
  const hasDirectedAttackSignal =
    /(c死|操死|干死|弄死|打死|杀了|杀死|砍死|捅死|揍你|揍死|暴打|打爆|打残|去死|骂你|喷你|怼你|草你|艹你|操你|干你|傻逼|滚|你全家|你的全家|你的母|问候你妈|问候你母)/.test(text);
