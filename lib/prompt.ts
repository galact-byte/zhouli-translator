function buildPerspectiveInstruction(text: string) {
  const firstPersonReplyPattern =
    /(^\s*我|我该|我如何|我怎么|我应该|如何回复|怎么回复|怎么说|我想|我做|我发|我给|我被|我的|我们|观众感谢我|别人夸我|粉丝|三连我的|我不想|我可以)/;
  const strongEmotionPattern =
    /(^\s*(我草|我操|卧槽|我靠|草泥马|妈的|艹|淦|服了|气死|破防|绷不住)|草泥马|傻逼|滚|去死|想骂人|想喷人|骂人.*周礼|喷人.*周礼|怼人.*周礼)/;

  if (strongEmotionPattern.test(text)) {
    return `本句视角判定：第一人称强情绪改写。
- 把这句话当作发言者本人正在对某事或某个隐含对象表达不满，输出必须以“我”的立场来写。
- 不要训斥发言者，不要写“你说出这句话/今天你/你这句话/你的事/你心中/让你”。
- 不要把输出写成“我劝自己冷静/我不愿失礼/我先按下怒气/我咽下怒火/我不骂了”的自我规训；要把原话的斥责功能翻译成礼法语言。
- 若原话没有明确对象，必须使用“阁下/此人/此事/眼前这般行径”等外部对象，不要把矛头转向发言者自己。
- 禁止写成自我反省或修身规训：不要出现“我乱了本心/我护不住体面/我该退后一步/我该重新想想/我该站好位置/我分寸守不住”这类意思。
- 好的方向是：把“我破防了/我靠/我草”译成“我今日不是无故动怒，而是此事已越过分寸”“阁下此举，实在亏了礼数”“眼前这般行径，叫人如何还能端坐不言”。
- 降低露骨攻击性，严禁复述侮辱词；即使原话是“他说我……/别人骂我……”，也只能写“粗鄙之语、污言秽语、禽兽之名、无礼之言”。保留愤怒、荒唐、不满和锋芒。`;
  }

  if (firstPersonReplyPattern.test(text)) {
    return `本句视角判定：第一人称代写。
- 把原话中的“我/我们”当作发言者本人，输出应像用户可以直接复制发送的话。
- 原话中属于“我/我们”的动作，必须继续归属于“我/我们”；不要把“我做了网站/我做视频/我的视频”改成“你做了网站/您做视频/他做了网站”。
- 原话中的“你、观众、粉丝、朋友、老板”等是对方或被谈论对象，不是发言者本人。
- 若原话是粗口、辱骂或强烈情绪，不能训斥发言者，不能写“你说出这句话/你这句话”；要改成“我心中有怒/我有不平/我愿把话说得有分寸”的第一人称表达，并降低露骨攻击性。`;
  }

  return `本句视角判定：按语境决定。
- 若这是影视梗、公共事件、第三方人物或他人行为，可以用第三视角评议。
- 若生成过程中发现原话其实是在问“我如何说/回复”，应立即改用第一人称代写。`;
}

